package cn.jxufe.domain;

public class Blog_type {

	
	private int blog_type_id;
	
	private String blog_type_name;
	
	private int blog_user_id;
	
	public int getBlog_type_id() {
		return blog_type_id;
	}
	public void setBlog_type_id(int blog_type_id) {
		this.blog_type_id = blog_type_id;
	}
	public String getBlog_type_name() {
		return blog_type_name;
	}
	public void setBlog_type_name(String blog_type_name) {
		this.blog_type_name = blog_type_name;
	}
	public int getBlog_user_id() {
		return blog_user_id;
	}
	public void setBlog_user_id(int blog_user_id) {
		this.blog_user_id = blog_user_id;
	}
	
	
}
